export function defaultOwnedCharacterIds(): string[] {
  return CHARACTERS.filter((character) => character.unlocked).map((character) => character.id);
}

export function isCharacterId(id: string): boolean {
  return CHARACTERS.some((character) => character.id === id);
}

export function getCharacter(id: string): CharacterDef {
  return CHARACTERS.find((c) => c.id === id) ?? CHARACTERS[0];
}
